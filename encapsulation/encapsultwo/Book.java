package encapsulation.encapsultwo;

public class Book {
    private String title;
    private String author;

    public void setTitle(String t){
        title = t;
    }

    public String getTitle() {
        return title;
    }

    public void setAuthor(String a) {
        author = a;
    }

    public String getAuthor() {
        return author;
    }
}
